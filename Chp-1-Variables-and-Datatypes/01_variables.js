// variable is a container in which we contain different data 
// types

let a = 5;
console.log(a);
a = "zain";           // In javascript variable can be change from one data type to other

console.log(a);

// Declaring variables
let name = "Zain";
let age = 18;
let isStudent = true;

// Displaying values using console.log
console.log("Name:", name);
console.log("Age:", age);
console.log("Is a Student?", isStudent);

// Changing values
age = 19;
isStudent = false;

// Display updated values
console.log("Updated Age:", age);
console.log("Updated Student Status:", isStudent);

// Rules for assigning variables

//1. letter, digits, underscore and $ sign allowed
//2. Must begin with letter, underscore _ or $ sign
//3. Javascript reserved words cannot be used as variable such as var let const etc
//4.Zain and zain are different(js is case sensitive)
